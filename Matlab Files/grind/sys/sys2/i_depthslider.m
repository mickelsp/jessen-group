      'Position',[50 47.25 60.75 18], ...
      'String','Cancel', ...
      'Tag','Pushbutton1');
   set(h0, 'userdata', ud);
   set(h0,'WindowStyle','modal');
   i_depthslider('changed', h0);
   uiwait;
   if ishandle(h0)
     ud= get(h0,'userdata');
     res=ud.curval';
     close(h0);
   else
      res=[];
   end;
 case 'ok'
   uiresume;
 case 'cancel'
    h=gcbf;
    if isempty(h)
        h=gcf;
    end;
    ud= get(h,'userdata');
    ud.curval=[];
    set(h,'userdata',ud)
   uiresume;
case 'changed'
   if nargin==2
      h=par1;
    else
    h=gcbf;
    if isempty(h)
        h=gcf;
     end;
    end;
    ud= get(h,'userdata');
    if ~isempty(ud)
    h1=findobj(h,'tag','DepthSlider');
    ud.depth=get(h1,'value');
    ud.curval=ud.initpnt(1,:)+ud.depth*(ud.initpnt(2,:)-ud.initpnt(1,:));
    h1=findobj(h,'tag','XText');
    set(h1,'string',sprintf('%s = %g',ud.labels{1},ud.curval(1)));
    h1=findobj(h,'tag','YText');
    set(h1,'string',sprintf('%s = %g',ud.labels{2},ud.curval(2)));
    h1=findobj(h,'tag','ZText');
    set(h1,'string',sprintf('%s = %g',ud.labels{3},ud.curval(3)));
    set(h,'userdata',ud);
    end;
 otherwise
end;
