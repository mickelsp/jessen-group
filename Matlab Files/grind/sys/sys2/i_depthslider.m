      'HorizontalAlignment','left', ...
      'ListboxTop',0, ...
      'Position',[10 19.5 45 18], ...
      'String','To the front', ...
      'Style','text', ...
      'Tag','StaticText2');
   uicontrol('Parent',h0, ...
      'Units','points', ...
      'BackgroundColor',[0.914 0.914 0.914], ...
      'HorizontalAlignment','left', ...
      'ListboxTop',0, ...
      'Position',[10 248.25 45 18], ...
      'String','To the back', ...
      'Style','text', ...
      'Tag','StaticText2');
   uicontrol('Parent',h0, ...
      'Units','points', ...
      'BackgroundColor',[0.914 0.914 0.914], ...
      'HorizontalAlignment','left', ...
      'ListboxTop',0, ...
      'Position',[50 164.25 100 18], ...
      'String','X=', ...
      'Style','text', ...
      'Tag','XText');
   uicontrol('Parent',h0, ...
      'Units','points', ...
