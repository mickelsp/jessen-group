% FIG-files.  Figures which have been annotated using the plot editor tools
% are incompatible with the M-file/MAT-file format, and should be saved as
% FIG-files.

if nargin == 0
   flag = 'init';
end;
switch flag
 case 'init'
   if nargin < 2
      par1 = get(gca, 'currentpoint');
      par2 = {get(get(gca,'xlabel'),'string'),get(get(gca,'ylabel'),'string'),get(get(gca,'zlabel'),'string')};
   end;
   pos=get(gcf,'position');
   ud.initpnt = par1;
   ud.labels =par2;
   ud.curval=[];
   ud.depth = 0;
   h0=figure('Color',[0.914 0.914 0.914], ...
      'MenuBar','none', ...
      'Name','Clicked in 3D figure', ...
      'NumberTitle','off', ...
      'PaperPosition',[18 180 576 432], ...
      'PaperUnits','points', ...
      'Position',[pos(1)+pos(3) pos(2) 180 426], ...
      'Tag','Fig1', ...
      'ToolBar','none');
   uicontrol('Parent',h0, ...
      'Units','points', ...
      'BackgroundColor',[1 1 1], ...
      'Callback','i_depthslider(''changed'')', ...
      'ListboxTop',0, ...
      'Position',[10 43.5 21.75 195.75], ...
      'Style','slider', ...
      'Tag','DepthSlider');
   uicontrol('Parent',h0, ...
      'Units','points', ...
      'BackgroundColor',[0.914 0.914 0.914], ...
      'HorizontalAlignment','left', ...
      'ListboxTop',0, ...
      'Position',[10 276.75 120.75 23.25], ...
      'String','Indicate the depth in the figure', ...
      'Style','text', ...
      'Tag','StaticText1');
   uicontrol('Parent',h0, ...
      'Units','points', ...
